    /// End-to-end: with a CONSUMER_WAT-style builtin at `inj.path` and
    /// `with_fake_builtins` priming a real config-provider template,
    /// `ensure_provider_for` should stamp the path and write a patched
    /// component whose magic-anchored body decodes back to the input
    /// values.
    #[test]
    fn ensure_provider_for_writes_patched_component() {
        // Build a real-looking template blob: prefix bytes + MAGIC_BYTES
        // + [u32 len=0] + padding. The patcher only cares about the
        // magic-anchored window, so the surrounding bytes are
        // arbitrary.
        let mut template = Vec::new();
        template.extend_from_slice(b"\0asm\x01\x00\x00\x00fake-prefix-");
        template.extend_from_slice(&MAGIC_BYTES);
        template.extend_from_slice(&0u32.to_le_bytes());
        template.extend(std::iter::repeat_n(
            0xAA,
            CAPACITY - MAGIC_LEN - LEN_PREFIX_BYTES,
        ));
        template.extend_from_slice(b"-fake-suffix");

        // The builtin is the WAT consumer; the provider template is the
        // synthetic blob above. Both live in SPLICER_BUILTINS_DIR.
        let consumer_bytes = wat::parse_str(CONSUMER_WAT).expect("wat");
        let splits = tempfile::tempdir().unwrap();
        let builtin_dir = splits.path().join("builtins");
        std::fs::create_dir_all(&builtin_dir).unwrap();
        let builtin_path = builtin_dir.join("fake-consumer.wasm");
        std::fs::write(&builtin_path, &consumer_bytes).unwrap();

        // Write the synthetic config-provider template to a temp
        // override dir. `with_fake_builtins` lets us prime overrides
        // for arbitrary names, but only with the magic-bytes fixture
        // — for this test we override directly via the env var so we
        // can plant our richer template.
        let override_dir = tempfile::tempdir().unwrap();
        std::fs::write(override_dir.path().join("config-provider.wasm"), &template).unwrap();
        let _guard = EnvGuard::set("SPLICER_BUILTINS_DIR", override_dir.path());

        let mut inj = Injection::from_path("metrics", builtin_path.to_str().unwrap());
        inj.builtin_config.insert("buffer".into(), "100".into());
        inj.builtin_config
            .insert("flush_after_seconds".into(), "10.0".into());

        ensure_provider_for(&mut inj, splits.path()).expect("ensure");

        let path = inj
            .config_provider_path
            .as_deref()
            .expect("provider path stamped");
        let patched = std::fs::read(path).expect("provider written");
        let parsed = parse_back(&patched);
        assert_eq!(parsed.get("buffer").map(String::as_str), Some("100"));
        assert_eq!(
            parsed.get("flush_after_seconds").map(String::as_str),
            Some("10.0")
        );
    }

    /// YAML with no `config:` block still has to produce a working
    /// provider when the builtin imports the substrate: empty
    /// `inj.builtin_config` → provider with `payload_len = 4`
    /// (`count = 0`) → runtime returns `None` for every key →
    /// consumer falls back to its hardcoded defaults. This exercises
    /// the path users hit when they just write `builtin: <name>`
    /// without any `config:` entries.
    #[test]
    fn ensure_provider_for_emits_provider_with_empty_config() {
        // Synthetic template: prefix bytes + MAGIC_BYTES + zero
        // payload_len + 0xAA padding. The patcher only cares about
        // the magic-anchored window.
        let mut template = Vec::new();
        template.extend_from_slice(b"\0asm\x01\x00\x00\x00fake-prefix-");
        template.extend_from_slice(&MAGIC_BYTES);
        template.extend_from_slice(&0u32.to_le_bytes());
        template.extend(std::iter::repeat_n(
            0xAA,
            CAPACITY - MAGIC_LEN - LEN_PREFIX_BYTES,
        ));

        let consumer_bytes = wat::parse_str(CONSUMER_WAT).expect("wat");
        let splits = tempfile::tempdir().unwrap();
        let builtin_dir = splits.path().join("builtins");
        std::fs::create_dir_all(&builtin_dir).unwrap();
        let builtin_path = builtin_dir.join("fake-consumer.wasm");
        std::fs::write(&builtin_path, &consumer_bytes).unwrap();

        let override_dir = tempfile::tempdir().unwrap();
        std::fs::write(override_dir.path().join("config-provider.wasm"), &template).unwrap();
        let _guard = EnvGuard::set("SPLICER_BUILTINS_DIR", override_dir.path());

        // No keys inserted — this is the "no `config:` in YAML" case.
        let mut inj = Injection::from_path("metrics", builtin_path.to_str().unwrap());
        assert!(inj.builtin_config.is_empty());

        ensure_provider_for(&mut inj, splits.path()).expect("ensure");

        let path = inj
            .config_provider_path
            .as_deref()
            .expect("provider path stamped even with empty config");
        let patched = std::fs::read(path).expect("provider written");
        let parsed = parse_back(&patched);
        assert!(
            parsed.is_empty(),
            "patched provider should decode to an empty table"
        );
        // Any lookup returns None — consumers fall back to defaults.
        assert!(!parsed.contains_key("buffer"));
    }

