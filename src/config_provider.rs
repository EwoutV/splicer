//! Splice-time patcher for the `splicer:builtin-config` provider
//! template.
//!
//! The provider template is built like any other builtin
//! (`builtins/config-provider/`). It exports
//! `splicer:builtin-config/get` and reads its key/value table from a
//! fixed-capacity `static` buffer in its core module. The Rust crate
//! pads that buffer with a known magic sentinel so a byte-scan can
//! locate it from the outside.
//!
//! `build_provider` loads the template bytes (override → cache → OCI),
//! serializes the caller's KV map into the table's wire format, and
//! overwrites the bytes after the magic. The data segment's total
//! length is preserved; the in-component parser uses the length field
//! to bound iteration so trailing padding stays inert.
//!
//! Why byte-scan instead of a structural rewrite (wasm-encoder)?
//! Same reason the original design picked patching over codegen:
//! per-splice work is one in-place edit, and the template is built
//! once at builtin-publish time.

