include!(concat!(env!("OUT_DIR"), "/builtin_config_constants.rs"));

// Wire-format constants AND codec (serialize_table / deserialize_table
// / read_u32_le) shared with the provider template
// (`builtins/config-provider/src/wire_format.rs`), which loads the
// same file via `mod wire_format;`. Do not duplicate the format here.
#[path = "../builtins/config-provider/src/wire_format.rs"]
mod wire_format;

use wire_format::{serialize_table, CAPACITY, LEN_PREFIX_BYTES, MAGIC_BYTES, MAGIC_LEN};

/// Name of the provider template builtin. Mirrors the crate dir under
/// `builtins/` (no canonical source above the directory name itself).
/// Referenced from `builtins::INTERNAL_BUILTINS` so users can't
/// inject it directly from YAML.
pub(crate) const PROVIDER_BUILTIN_NAME: &str = "config-provider";

/// Bytes the serialized table can use after the magic + length header.
const MAX_PAYLOAD: usize = CAPACITY - MAGIC_LEN - LEN_PREFIX_BYTES;

/// If `injection.path` points at a wasm component that imports
/// `splicer:builtin-config`, build a patched provider with
/// `injection.builtin_config` baked in, write it under
/// `<splits_dir>/builtins/<wac_var>-config.wasm`, and stamp the
/// resulting path onto `injection.config_provider_path`.
///
/// Idempotent: if `injection.config_provider_path` is already set,
/// returns without re-doing the work. Returns without writing when
/// the injection isn't a builtin, has no materialized path yet, or
/// doesn't import the substrate.
pub fn ensure_provider_for(injection: &mut Injection, splits_dir: &Path) -> Result<()> {
    if injection.config_provider_path.is_some() {
        return Ok(());
    }
    let Some(builtin_path) = injection.path.as_deref() else {
        return Ok(());
    };
    let bytes = std::fs::read(builtin_path)
        .with_context(|| format!("Failed to read materialized builtin '{}'", builtin_path))?;
    if !imports_substrate(&bytes) {
        return Ok(());
    }
    let provider_bytes = build_provider(&injection.builtin_config).with_context(|| {
        format!(
            "Failed to build config provider for injection '{}'",
            injection.name
        )
    })?;

    // Write alongside the materialized builtin under `<splits>/builtins/`.
    // The dir is created by `materialize_into` before this runs, but the
    // create-all keeps the function self-contained in case a caller wires
    // it independently of the standard pipeline.
    let dir = splits_dir.join("builtins");
    std::fs::create_dir_all(&dir)
        .with_context(|| format!("Failed to create builtins dir: {}", dir.display()))?;
    let out = dir.join(format!("{}-config.wasm", injection.name));
    std::fs::write(&out, &provider_bytes)
        .with_context(|| format!("Failed to write config provider: {}", out.display()))?;

    let out_str = out
        .to_str()
        .ok_or_else(|| {
            anyhow::anyhow!(
                "config provider path contains non-UTF-8 bytes: {}",
                out.display()
            )
        })?
        .to_string();
    injection.config_provider_path = Some(out_str);
    Ok(())
}

/// True iff the component bytes declare an import of any interface in
/// the `splicer:builtin-config` package. Best-effort: returns `false`
/// on decode errors, mirroring `resource_bearing_imports` in
/// `src/wac.rs`. The downstream consequence of a false negative is
/// the same as for a builtin that genuinely doesn't import the
/// substrate — no provider is wired — so a hard failure isn't useful.
fn imports_substrate(bytes: &[u8]) -> bool {
    let Ok(decoded) = std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| {
        wit_component::decode(bytes)
    })) else {
        return false;
    };
    let Ok(wit_component::DecodedWasm::Component(resolve, world_id)) = decoded else {
        return false;
    };
    let world = &resolve.worlds[world_id];
    for (_key, item) in &world.imports {
        let wit_parser::WorldItem::Interface { id, .. } = item else {
            continue;
        };
        let Some(name) = resolve.id_of(*id) else {
            continue;
        };
        // `id_of` returns the qualified name (e.g.
        // `splicer:builtin-config/get@0.1.0`); the package prefix +
        // `/` test isolates this package without coupling the check
        // to whatever interface(s) it ends up exposing later.
        if name.starts_with(&format!("{BUILTIN_CONFIG_PACKAGE}/")) {
            return true;
        }
    }
    false
}

/// Build a patched provider component with `values` baked in.
///
/// Returns the wasm bytes ready to write to disk and reference from
/// the WAC composition. The fully-qualified WIT export the bytes
/// satisfy is `splicer:builtin-config/get@0.1.0`.
///
/// Fails if:
/// - The provider template can't be resolved (no override + no cache +
///   no network).
/// - The magic sentinel doesn't appear exactly once in the template.
///   This signals the template was rebuilt against an out-of-date
///   `MAGIC`, or that the build elided the static.
/// - The serialized table exceeds the reserved capacity.
pub fn build_provider(values: &BTreeMap<String, String>) -> Result<Vec<u8>> {
    let mut bytes = builtins::load_resolved_bytes(PROVIDER_BUILTIN_NAME)
        .context("Failed to load config-provider template bytes")?;
    patch_in_place(&mut bytes, values)?;
    Ok(bytes)
}

/// Locate `MAGIC_BYTES` in `bytes` and overwrite the bytes that
/// follow with the serialized KV table.
fn patch_in_place(bytes: &mut [u8], values: &BTreeMap<String, String>) -> Result<()> {
    let payload = serialize_table(values);
    if payload.len() > MAX_PAYLOAD {
        anyhow::bail!(
            "config-provider payload of {} bytes exceeds reserved capacity of {} bytes \
             (template `CAPACITY` minus magic + length header). Either trim the config or \
             rebuild the provider with a larger `CAPACITY`.",
            payload.len(),
            MAX_PAYLOAD,
        );
    }
    let offset = find_unique_magic(bytes)?;
    // [magic][u32 LE payload_len][payload bytes][unchanged padding]
    let len_off = offset + MAGIC_LEN;
    let payload_off = len_off + LEN_PREFIX_BYTES;
    let payload_end = payload_off + payload.len();
    bytes[len_off..len_off + LEN_PREFIX_BYTES]
        .copy_from_slice(&(payload.len() as u32).to_le_bytes());
    bytes[payload_off..payload_end].copy_from_slice(&payload);
    Ok(())
}

/// Return the byte offset of the single occurrence of `MAGIC_BYTES`
/// in `bytes`. Fails if the magic isn't found or appears more than
/// once (either is a template/build problem the user can't fix by
/// retrying — surface clearly).
fn find_unique_magic(bytes: &[u8]) -> Result<usize> {
    let mut found: Option<usize> = None;
    let mut i = 0;
    while i + MAGIC_LEN <= bytes.len() {
        if bytes[i..i + MAGIC_LEN] == MAGIC_BYTES {
            if found.is_some() {
                anyhow::bail!(
                    "config-provider template has multiple `MAGIC_BYTES` matches. The byte-scan \
                     patcher assumes exactly one. Check the template build."
                );
            }
            found = Some(i);
            // Magic is long enough that it can't overlap with itself;
            // skip past to keep the scan O(n).
            i += MAGIC_LEN;
        } else {
            i += 1;
        }
    }
    found.ok_or_else(|| {
        anyhow::anyhow!(
            "config-provider template is missing the magic sentinel. The template was \
             either built against an out-of-date `MAGIC_BYTES`, or the compiler elided \
             the KV buffer. Rebuild builtins/config-provider with the in-tree sources."
        )
    })
}

